    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
           
            con = DriverManager.getConnection("jdbc:sqlite:Gam.db");
            String sql = "select username , password from gam_users where username = ? and password = ?";
            
            PreparedStatement pr = con.prepareStatement(sql);
            
            pr.setString(1, jTextField1.getText());
            pr.setString(2, jTextField2.getText());
            
            ResultSet rs = pr.executeQuery();
            
            if (rs.next()) {
                    this.setVisible(false);
                    Mainfarme m = new Mainfarme();
                    m.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, " بطل تحوير انت مش الكابتن و مش هدخلك");
               
            }
        } catch (SQLException ex) {
            System.err.println(ex.getMessage());
        }finally{
            try {
                con.close();
            } catch (SQLException ex) {
                Logger.getLogger(LoginFrame1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

